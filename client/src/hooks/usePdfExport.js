import { useState, useCallback } from 'react';

const riskHex = (level) => ({ dangerous: '#ff2244', medium: '#ff8c00', low: '#ffdd00', safe: '#00ff88' }[level] || '#ff2244');
const hexToRgb = (hex) => {
  const h = hex.replace('#', '');
  return [
    parseInt(h.slice(0, 2), 16),
    parseInt(h.slice(2, 4), 16),
    parseInt(h.slice(4, 6), 16),
  ];
};

