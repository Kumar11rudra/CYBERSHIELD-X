      doc.setFontSize(7);
      doc.text(`DATE GENERATED  :  ${new Date().toLocaleString()}`, W - 14, 18, { align: 'right' });
      doc.text(`AUTHORIZED SIG  :  ${user?.username?.toUpperCase() || 'SYSTEM'}`, W - 14, 24, { align: 'right' });
      
